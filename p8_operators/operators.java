// package p8_operators;

public class operators {

    public static void main(String [] args){

        // Assisgnement operator

        int a = 8;

        // Arithmetic operator 
        int b = a*4 ;

        // Comaparison operator 
        System.out.println(a==b) ;

        // Logical operator
        System.out.println(a>5 && a>10);
        System.out.println(a>5 || a>10) ;

        // bitwise operator 
        // bitwise operator converts values into bits
        System.out.println(2&3) ;

    }
    
}
